function Container() {
  const navigate = useNavigate();
  const validationSchema = Yup.object({
    email: Yup.string().email('Invalid email').required('Required'),
    password: Yup.string().min(6, 'Too Short').required('Required'),
  });

  return (
    <Formik
      initialValues={{ email: '', password: '' }}
      validationSchema={validationSchema}
      onSubmit={async (values, { setSubmitting, setFieldError }) => {
        try {
          const res = await fetch('http://localhost:5000/auth/login', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify(values),
          });
          const data = await res.json();
          if (res.ok) {
            localStorage.setItem('token', data.token);
            if (data.user && data.user.username) {
              localStorage.setItem('username', data.user.username);
            }
            navigate('/');
          } else {
            setFieldError('password', data.message || 'Login failed');
          }
        } catch (err) {
          setFieldError('password', 'Server error. Please try again later.');
        } finally {
          setSubmitting(false);
        }
      }}
    >
      {({ handleSubmit, isSubmitting }) => (
        <div className="content-stretch flex flex-col gap-4 items-start w-full max-w-[360px]" data-name="Container">
          <Logo />
          <p className="font-['Inter:Semi_Bold',sans-serif] font-semibold leading-tight min-w-full not-italic relative shrink-0 text-3xl text-black">Log in to your account</p>
          <Form className="w-full flex flex-col gap-4" onSubmit={handleSubmit}>
            <div className="w-full">
              <div className="relative rounded-lg shrink-0 w-full h-11" data-name="Form/Input/Email">
                <div className="content-stretch flex gap-3 items-center overflow-clip px-4 py-2 relative rounded-[inherit] size-full">
                  <MdiLightEmail />
                  <Field type="email" name="email" placeholder="Email" className="font-['Inter:Extra_Light',sans-serif] font-light bg-transparent outline-none leading-normal not-italic relative shrink-0 text-sm text-black w-full" />
                </div>
                <div aria-hidden="true" className="absolute border border-[rgba(135,135,135,0.4)] border-solid inset-0 pointer-events-none rounded-lg" />
              </div>
              <ErrorMessage name="email" component="div" className="text-red-500 text-sm mt-1" />
            </div>

            <div className="w-full">
              <div className="relative rounded-lg shrink-0 w-full h-11" data-name="Form/Input/Password">
                <div className="content-stretch flex gap-3 items-center overflow-clip px-4 py-2 relative rounded-[inherit] size-full">
                  <TeenyiconsPasswordOutline />
                  <Field type="password" name="password" placeholder="Password" className="font-['Inter:Extra_Light',sans-serif] font-light bg-transparent outline-none leading-[normal] not-italic relative shrink-0 text-sm text-black w-full" />
                </div>
                <div aria-hidden="true" className="absolute border border-[rgba(135,135,135,0.4)] border-solid inset-0 pointer-events-none rounded-lg" />
              </div>
              <ErrorMessage name="password" component="div" className="text-red-500 text-sm mt-1" />
            </div>

            <button type="submit" disabled={isSubmitting} className="bg-[#0034d1] mt-2 cursor-pointer content-stretch flex items-center justify-center py-2.5 relative rounded-md shrink-0 w-full hover:bg-blue-700 transition" data-name="Button/Large/Fill">
              <p className="font-['Open_Sans:SemiBold',sans-serif] font-semibold leading-none relative shrink-0 text-base text-white whitespace-nowrap" style={{ fontVariationSettings: "'wdth' 100" }}>
                {isSubmitting ? 'Logging in...' : 'Login'}
              </p>
            </button>
          </Form>
          <Frame2 />
          <Container4 />
          <div className="content-stretch flex items-center justify-center relative shrink-0 w-full" data-name="Link">
            <Text />
            <Link />
          </div>
        </div>
      )}
    </Formik>
  );
}

